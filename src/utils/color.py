from colored import Fore, Style


def red(text):
    return f"{Fore.red}{text}{Style.reset}"


def green(text):
    return f"{Fore.green}{text}{Style.reset}"


def orange(text):
    return f"{Fore.orange}{text}{Style.reset}"


def blue(text):
    return f"{Fore.blue}{text}{Style.reset}"


def yellow(text):
    return f"{Fore.yellow}{text}{Style.reset}"


def magenta(text):
    return f"{Fore.magenta}{text}{Style.reset}"


def cyan(text):
    return f"{Fore.cyan}{text}{Style.reset}"


def white(text):
    return f"{Fore.white}{text}{Style.reset}"
